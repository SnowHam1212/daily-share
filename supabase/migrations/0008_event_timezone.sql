-- 0008_event_timezone.sql
-- 予定にタイムゾーン（IANA tz 名）を保存する列を追加。
--
-- startAt/endAt は timestamptz（瞬時）で保存しているため、時刻つき
-- 予定は閲覧者のローカル時刻へ自動変換される。本列は「その予定が
-- どの TZ を基準に作られたか」を記録し、表示時の併記や、終日予定の
-- 日付境界の判定に用いる。
-- 既存行は NULL（= レガシー: 閲覧者ローカルで従来どおり扱う）。
-- ============================================================

ALTER TABLE events ADD COLUMN IF NOT EXISTS timezone text;
